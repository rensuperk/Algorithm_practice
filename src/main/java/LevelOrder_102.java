    public List<List<Integer>> levelOrder(TreeNode root) {
        List<List<Integer>> result = new ArrayList<>();
        if (root == null){
            return result;
        }
        Queue<TreeNode> queue = new LinkedList<>();
        queue.add(root);
        TreeNode end = root;
        TreeNode nextEnd = null;
        List<Integer> top = new ArrayList<>();
        result.add(top);
        while (!queue.isEmpty()){
            TreeNode cur = queue.poll();
            top.add(cur.val);
            if (cur.left != null){
                queue.add(cur.left);
                nextEnd = cur.left;
            }
            if (cur.right != null){
                queue.add(cur.right);
                nextEnd = cur.right;
            }
            //ä¸‹ä¸€å±‚æ²¡æœ‰å€¼,é˜²æ­¢å¤šç”Ÿæˆä¸€ä¸ªlist
            if (cur == end && nextEnd != null && end != nextEnd ){
                top = new ArrayList<>();
                result.add(top);
                end = nextEnd;
            }
        }
        return result;
    }
